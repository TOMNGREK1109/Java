package M11;

public class Mondai11n3 {
    public static void main(String[] args) {
        int x =100;
        int y =200; 
        int sum = 0;
        for (int i = x; i <= y; i++){
            sum = sum + i;
        }

        System.out.println(x + "から" + y +"までの合計値は"+ sum +"です。" );
    }
}